# Encoded flag

encoded_flag = [
    0xc9, 0xd9, 0xce, 0xd1, 0xce, 0x9e, 0xd3, 0xf5,
    0x98, 0xf5, 0xe2, 0x9a, 0xdd, 0xf5, 0xd8, 0xf5, 
    0xdf, 0x95, 0xd7
]

# XOR each byte with 0xaa to decode
decoded_flag = ''.join(chr(byte ^ 0xaa) for byte in encoded_flag)
print("Decoded flag:", decoded_flag)

